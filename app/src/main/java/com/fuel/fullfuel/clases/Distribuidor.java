package com.fuel.fullfuel.clases;

public class Distribuidor {
    private String nombre;
    private String logo;
    private String logo_svg;
    private String logo_horizontal_svg;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getLogo_svg() {
        return logo_svg;
    }

    public void setLogo_svg(String logo_svg) {
        this.logo_svg = logo_svg;
    }

    public String getLogo_horizontal_svg() {
        return logo_horizontal_svg;
    }

    public void setLogo_horizontal_svg(String logo_horizontal_svg) {
        this.logo_horizontal_svg = logo_horizontal_svg;
    }
}
