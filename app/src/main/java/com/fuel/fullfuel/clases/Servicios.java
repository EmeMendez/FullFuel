package com.fuel.fullfuel.clases;

public class Servicios {
        private Boolean  tienda;
        private Boolean  farmacia;
        private Boolean  mantencion;
        private Boolean  autoservicio;

    public Boolean getTienda() {
        return tienda;
    }

    public void setTienda(Boolean tienda) {
        this.tienda = tienda;
    }

    public Boolean getFarmacia() {
        return farmacia;
    }

    public void setFarmacia(Boolean farmacia) {
        this.farmacia = farmacia;
    }

    public Boolean getMantencion() {
        return mantencion;
    }

    public void setMantencion(Boolean mantencion) {
        this.mantencion = mantencion;
    }

    public Boolean getAutoservicio() {
        return autoservicio;
    }

    public void setAutoservicio(Boolean autoservicio) {
        this.autoservicio = autoservicio;
    }
}
